package Modelo;

/**
 *
 * @author cesar
 */
public class DBLINK {
    private String name,user,password,ip,port;

    public DBLINK(String name, String user, String password, String ip, String port) {
        this.name = name;
        this.user = user;
        this.password = password;
        this.ip = ip;
        this.port = port;
    }

    public DBLINK() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }
    
    
}
